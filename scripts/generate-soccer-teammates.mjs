import { readdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const CACHE = join(ROOT, ".cache", "soccer-data", "uefa", "match-stats");
const PROVENANCE = join(ROOT, "shared", "src", "soccerPlayers.provenance.json");
const OUTPUT = join(ROOT, "shared", "src", "soccerTeammates.generated.ts");
const VERIFY_ONLY = process.argv.includes("--verify");

const TARGET_PAIR = [
  "1992-04-10:sadio mane",
  "1998-10-07:trent alexander arnold",
];

const pairKey = (a, b) => JSON.stringify([a, b].sort());

function appearedInMatch(row) {
  const statistics = row.statistics ?? [];
  return statistics.some((stat) =>
    (stat.name === "matches_appearance" || stat.name === "minutes_played_official") && Number(stat.value) > 0
  );
}

async function buildPairKeys(manifest) {
  const identityByPlayerId = new Map();
  for (const player of manifest.players) {
    for (const playerId of player.sourcePlayerIds) {
      identityByPlayerId.set(String(playerId), player.sourceIdentity);
    }
  }

  const matchIds = [...new Set(manifest.players.flatMap((player) => player.sourceMatchIds))].sort();
  const pairs = new Set();
  for (const matchId of matchIds) {
    const rows = JSON.parse(await readFile(join(CACHE, `${matchId}.json`), "utf8"));
    const identitiesByTeam = new Map();
    for (const row of rows) {
      const identity = identityByPlayerId.get(String(row.playerId));
      if (!identity || !appearedInMatch(row)) continue;
      const teamId = String(row.teamId);
      const identities = identitiesByTeam.get(teamId) ?? new Set();
      identities.add(identity);
      identitiesByTeam.set(teamId, identities);
    }

    for (const identities of identitiesByTeam.values()) {
      const teammates = [...identities].sort();
      for (let i = 0; i < teammates.length; i++) {
        for (let j = i + 1; j < teammates.length; j++) {
          pairs.add(pairKey(teammates[i], teammates[j]));
        }
      }
    }
  }

  const pairKeys = [...pairs].sort();
  if (!pairs.has(pairKey(...TARGET_PAIR))) {
    throw new Error("Expected verified Liverpool teammate link between Sadio Mane and Trent Alexander-Arnold.");
  }
  return { pairKeys, revision: `${manifest.revision}-coappearance-v1` };
}

function generatedSource(pairKeys, revision) {
  return `// Generated by scripts/generate-soccer-teammates.mjs from cached UEFA match statistics.\n` +
    `// A pair is included only when both selected players appeared for the same club in the same UEFA match.\n` +
    `export const SOCCER_TEAMMATE_SOURCE_REVISION = ${JSON.stringify(revision)};\n\n` +
    `export const SOCCER_TEAMMATE_PAIR_KEYS = ${JSON.stringify(pairKeys, null, 2)} as const;\n\n` +
    `const SOCCER_TEAMMATE_PAIRS: ReadonlySet<string> = new Set(SOCCER_TEAMMATE_PAIR_KEYS);\n\n` +
    `function pairKey(a: string, b: string): string {\n` +
    `  return JSON.stringify([a, b].sort());\n` +
    `}\n\n` +
    `export function areSoccerTeammates(identityA: string, identityB: string): boolean {\n` +
    `  return identityA !== identityB && SOCCER_TEAMMATE_PAIRS.has(pairKey(identityA, identityB));\n` +
    `}\n`;
}

if (VERIFY_ONLY) {
  const committed = await readFile(OUTPUT, "utf8");
  const manifest = JSON.parse(await readFile(PROVENANCE, "utf8"));
  const revisionMatch = committed.match(/SOCCER_TEAMMATE_SOURCE_REVISION = ("[^"]+")/);
  const pairsMatch = committed.match(/SOCCER_TEAMMATE_PAIR_KEYS = ([\s\S]*?) as const;/);
  if (!revisionMatch || !pairsMatch) throw new Error("Committed football teammate ledger has an invalid shape.");
  const revision = JSON.parse(revisionMatch[1]);
  const pairKeys = JSON.parse(pairsMatch[1]);
  const validIdentities = new Set(manifest.players.map((player) => player.sourceIdentity));
  if (revision !== `${manifest.revision}-coappearance-v1`) throw new Error("Football teammate source revision is stale.");
  if (pairKeys.length === 0 || new Set(pairKeys).size !== pairKeys.length) throw new Error("Football teammate ledger is empty or contains duplicates.");
  for (const key of pairKeys) {
    const pair = JSON.parse(key);
    if (pair.length !== 2 || pair[0] >= pair[1] || !pair.every((identity) => validIdentities.has(identity))) {
      throw new Error(`Invalid football teammate pair: ${key}`);
    }
  }
  if (!pairKeys.includes(pairKey(...TARGET_PAIR))) throw new Error("Football teammate ledger is missing the Mané/Trent regression pair.");

  const cachedFiles = new Set(await readdir(CACHE).catch(() => []));
  const matchIds = [...new Set(manifest.players.flatMap((player) => player.sourceMatchIds))];
  if (matchIds.every((matchId) => cachedFiles.has(`${matchId}.json`))) {
    const reconstructed = await buildPairKeys(manifest);
    if (committed !== generatedSource(reconstructed.pairKeys, reconstructed.revision)) {
      throw new Error("Committed football teammate ledger is stale. Run npm run data:soccer-teammates.");
    }
  }
  console.log(`Football teammate data verified offline: ${pairKeys.length} same-club UEFA appearance pairs.`);
} else {
  const manifest = JSON.parse(await readFile(PROVENANCE, "utf8"));
  const generated = await buildPairKeys(manifest);
  const expected = generatedSource(generated.pairKeys, generated.revision);
  await writeFile(OUTPUT, expected);
  console.log(`Generated ${generated.pairKeys.length} verified football teammate pairs at ${OUTPUT}`);
}
