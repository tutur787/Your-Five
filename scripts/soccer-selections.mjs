const HISTORICAL_SOURCE = "https://www.uefa.com/news/01f9-0e7b894d6108-cf92003088c7-1000--uefa-com-users-team-of-the-year-2011-revealed/";

const roleEntries = (role, names) => names.map((name) => ({ name, role }));

function fanXi(year, sourceUrl, goalkeeper, defenders, midfielders, attackers) {
  return {
    key: `TOTY${year}`,
    label: `UEFA.com Fans' Team of the Year ${year}`,
    selection: "fan-team",
    window: { from: `${year}-01-01`, to: `${year}-12-31`, competition: "uefa-club" },
    sourceUrl,
    entries: [
      ...roleEntries("GK", goalkeeper),
      ...roleEntries("DEF", defenders),
      ...roleEntries("MID", midfielders),
      ...roleEntries("ATT", attackers),
    ],
  };
}

function observerSelection(key, label, sourceUrl, from, to, goalkeeper, defenders, midfielders, attackers, selection = "observer-team") {
  return {
    key,
    label,
    selection,
    window: { from, to, competition: "ucl" },
    sourceUrl,
    entries: [
      ...roleEntries("GK", goalkeeper),
      ...roleEntries("DEF", defenders),
      ...roleEntries("MID", midfielders),
      ...roleEntries("ATT", attackers),
    ],
  };
}

